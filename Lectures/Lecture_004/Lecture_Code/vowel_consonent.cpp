#include <iostream>
using namespace std;

int main()
{
    char c;
    cout << "Enter a character: ";
    cin >> c;

    if (c == 'a')
        cout << "Vowel";
    else if (c == 'e')
        cout << "Vowel";
    else if (c == 'i')
        cout << "Vowel";
    else if (c == 'o')
        cout << "Vowel";
    else if (c == 'u')
        cout << "Vowel";
    else
        cout << "Consonant";
    
    return 0;
}